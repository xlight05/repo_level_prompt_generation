package br.uniceub.fsw.html;

/**
 *
 * @author Hiragi
 */
public class SubmitButton {

    private String classe;
    private String value;

    public SubmitButton(String value) {
        this.classe = "";
        this.value = value;
    }

    public SubmitButton(String classe, String value) {
        this.classe = classe;
        this.value = value;
    }

    public String getHTML() {
        return "<INPUT TYPE='SUBMIT' CLASS='" + classe + "' VALUE='" + value + "'/>";
    }
}
