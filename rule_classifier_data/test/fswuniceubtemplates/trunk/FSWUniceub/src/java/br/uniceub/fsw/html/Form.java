package br.uniceub.fsw.html;

/**
 *
 * @author Hiragi
 */
public class Form {

    private String name;
    private String action;
    private String conteudo;

    public Form() {
    }

    public Form(String name, String action, String conteudo) {
        this.name = name;
        this.action = action;
        this.conteudo=conteudo;
    }

    public String getHTML(){
        return "<FORM ID='" + name + "' ACTION='"+action+"'/>\r\n" + conteudo + "\r\n</FORM>";
    }
}
