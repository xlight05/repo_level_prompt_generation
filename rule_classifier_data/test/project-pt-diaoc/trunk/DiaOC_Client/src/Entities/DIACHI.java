package Entities;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class DIACHI {
    private String MaDiaChi = "";
    private String MaThanhPho = "";
    private String MaQuan = "";
    private String MaPhuong = "";
    private String MaDuong = "";
    private String DiaChiCT = "";
    
    public DIACHI(){}

    public DIACHI(String maDiaChi, String maThanhPho, String maQuan, String maPhuong, String maDuong, String diaChiCT) {
        MaDiaChi = maDiaChi;
        MaThanhPho = maThanhPho;
        MaQuan = maQuan;
        MaPhuong = maPhuong;
        MaDuong = maDuong;
        DiaChiCT = diaChiCT;
    }

    @XmlElement
    public String getMaDiaChi() {
        return MaDiaChi;
    }

    public void setMaDiaChi(String maDiaChi) {
        this.MaDiaChi = maDiaChi;
    }

   
    @XmlElement
    public String getMaThanhPho() {
        return MaThanhPho;
    }

    public void setMaThanhPho(String MaThanhPho) {
        this.MaThanhPho = MaThanhPho;
    }

    @XmlElement
    public String getMaQuan() {
        return MaQuan;
    }

    public void setMaQuan(String MaQuan) {
        this.MaQuan = MaQuan;
    }

    @XmlElement
    public String getMaPhuong() {
        return MaPhuong;
    }

    public void setMaPhuong(String MaPhuong) {
        this.MaPhuong = MaPhuong;
    }

    @XmlElement
    public String getMaDuong() {
        return MaDuong;
    }

    public void setMaDuong(String MaDuong) {
        this.MaDuong = MaDuong;
    }

    @XmlElement
    public String getDiaChiCT() {
        return DiaChiCT;
    }

    public void setDiaChiCT(String DiaChiCT) {
        this.DiaChiCT = DiaChiCT;
    }
    
}
