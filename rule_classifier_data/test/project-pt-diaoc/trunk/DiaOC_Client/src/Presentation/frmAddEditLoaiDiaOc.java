    private void LoadData()throws Exception{
        try{
            ArrayList<LOAIDIAOC> lst = dbLoaiDiaOc.GetAllRows();
            DefaultTableModel dtm = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int x,int y){
                    if(y==0||y==1)
                        return false;
                    return true;
                }
            };
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("MÃ"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("name"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("sign"));
            for(int i = 0; i < lst.size(); i++)
            {
                LOAIDIAOC _loai = lst.get(i);
                Vector _v = new Vector();
                _v.add(_loai.getMaLoaiDiaOc());
                _v.add(_loai.getTenLoaiDiaOc().trim());
                _v.add(_loai.getKyHieu());
                dtm.addRow(_v);
            }
            this.tblLoaiDiaOc.setModel(dtm);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.toString()+this.getClass().toString());
        } 
    }
