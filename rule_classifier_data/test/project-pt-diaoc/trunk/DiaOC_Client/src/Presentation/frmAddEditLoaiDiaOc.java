    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = tbxLoaiDiaOc.getText().trim();
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("must enter property type"));
        }else{
            try
            {               
                dbLoaiDiaOc.Insert(name,tbxSymbol.getText().trim());                
                this.LoadData();
                tbxLoaiDiaOc.setText("");
                tbxSymbol.setText("");
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("added a property type"));
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.toString());
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String name = tbxLoaiDiaOc.getText().trim();
        if(tblLoaiDiaOc.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("must choice property type to update"));
        }else if(name.isEmpty()){
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("must enter property type"));
        }else{
            try{     
                dbLoaiDiaOc.Update((String)this.tblLoaiDiaOc.getValueAt(this.tblLoaiDiaOc.getSelectedRow(), 0),
                        name, tbxSymbol.getText().trim());
                this.LoadData();
                tbxLoaiDiaOc.setText("");
                tbxSymbol.setText("");
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("updated a property type"));
            } catch(Exception e){
                JOptionPane.showMessageDialog(this, e.toString());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

