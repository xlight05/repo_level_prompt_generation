    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(tblLoaiDiaOc.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("must choice property type to delete"));
        }else{
            int i = JOptionPane.showConfirmDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("really delete"), java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("warn"),
                    JOptionPane.YES_NO_OPTION);
            if(i == 0){
                try{
                    dbLoaiDiaOc.Delete((String)tblLoaiDiaOc.getValueAt(tblLoaiDiaOc.getSelectedRow(), 0));
                    this.LoadData();
                    tbxLoaiDiaOc.setText("");
                    tbxSymbol.setText("");
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.toString());
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    

