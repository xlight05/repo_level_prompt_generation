    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String viTri = tbxViTri.getText().trim();
        String khoIn = tbxKhoIn.getText().trim();
        String mauSac = tbxMauSac.getText().trim();
        String ghiChu = tbxGhiChu.getText().trim();
        String giaTien = tbxGia.getText().trim();
           if(viTri.isEmpty())
                JOptionPane.showMessageDialog(this,
                        java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenterlocation"));
            else
                if(khoIn.isEmpty())
                    JOptionPane.showMessageDialog(this,
                            java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenterprintsize"));
                else
                    if(mauSac.isEmpty())
                        JOptionPane.showMessageDialog(this,
                                java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notentercolor"));
                    else
                        if(giaTien.isEmpty())
                            JOptionPane.showMessageDialog(this,
                                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notentercost"));
                        else
                        {
                            try
                            {
                                GiaTienQCBaoRSc _giaTien = new GiaTienQCBaoRSc();
                                Float giaTienBao = Float.MIN_VALUE;
                                try{
                                    giaTienBao = Float.parseFloat(giaTien);
                                    _giaTien.Insert(viTri, khoIn, mauSac, ghiChu, giaTienBao);
                                    this.LoadData();
                                    clear();
                                    JOptionPane.showMessageDialog(this,
                                            java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("insertedGiaTienBao"));
                                }catch(NumberFormatException e){
                                    JOptionPane.showMessageDialog(this,
                                            java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("nhapsoGiaTien"));
                                }
                            }catch(Exception e){
                                JOptionPane.showMessageDialog(this, e.toString());
                            }
                        }
    }//GEN-LAST:event_btnAddActionPerformed

