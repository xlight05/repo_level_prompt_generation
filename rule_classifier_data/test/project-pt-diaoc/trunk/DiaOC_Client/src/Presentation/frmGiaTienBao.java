    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(this.tblGiaTienBao.getSelectedRow()==-1)
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("mustselectGiaTien"));
        else
        {
            int n = JOptionPane.showConfirmDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("really delete"),
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("warn"),JOptionPane.YES_NO_OPTION);
            if(n==0)
            {
                try
                {
                    GiaTienQCBaoRSc _giaTien = new GiaTienQCBaoRSc();
                    _giaTien.Delete((String)this.tblGiaTienBao.getValueAt(this.tblGiaTienBao.getSelectedRow(), 1));
                    this.LoadData();
                    this.clear();
                    JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("deleted"));
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.toString());
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tbxGiaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbxGiaKeyTyped
        // TODO add your handling code here:
        String decimal = "1234567890.";
        int key = evt.getKeyChar();
        if(key!=KeyEvent.VK_BACK_SPACE&&key!=KeyEvent.VK_DELETE&&key!=KeyEvent.VK_ENTER&&key!=KeyEvent.VK_END&&key!=KeyEvent.VK_HOME&&key!=KeyEvent.VK_TAB)
        {
            int flag=0;
            if(decimal.indexOf(key)==-1)
            {
                flag++;
                JOptionPane.showMessageDialog(this,
                        java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("nhapsoGiaTien"));
            }
            if(flag>0)
                evt.consume();
        }
    }//GEN-LAST:event_tbxGiaKeyTyped
    private void LoadData(){
        try
        {
            GiaTienQCBaoRSc _giaTien = new GiaTienQCBaoRSc();
            ArrayList<GIATIEN_QC_BAO> lst = _giaTien.GetAllRows();
            DefaultTableModel dtm = new DefaultTableModel(){
                @Override
                public boolean  isCellEditable(int x,int y){
                    return false;
                }
            };
            dtm.addColumn("STT");
            dtm.addColumn("Mã Giá Tiền");
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("location"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("printsize"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("color"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("cost"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("note"));
            for(int i=0;i<lst.size();i++)
            {
                Vector _v = new Vector();
                GIATIEN_QC_BAO _gia = lst.get(i);
                _v.add(i+1);
                _v.add(_gia.getMaGiaTien());
                _v.add(_gia.getViTri().trim());
                _v.add(_gia.getKhoIn().trim());
                _v.add(_gia.getMauSac().trim());
                _v.add(_gia.getGiaTien());
                _v.add(_gia.getGhiChu().trim());
                dtm.addRow(_v);
            }
            this.tblGiaTienBao.setModel(dtm);
            this.tblGiaTienBao.getColumnModel().getColumn(0).setPreferredWidth(30);
            this.tblGiaTienBao.getColumnModel().getColumn(6).setPreferredWidth(140);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }
