            String maQuan = ((QUAN)cbxQuan.getSelectedItem()).getMaQuan();
            String maPhuong = ((PHUONG)cbxPhuong.getSelectedItem()).getMaPhuong();
            String maDuong = ((DUONG)cbxDuong.getSelectedItem()).getMaDuong();
