    private boolean CheckDiaOc(){
        String diaChi = tbxDiaChiCTDiaOc.getText();
        String dienTichDat = tbxDienTichDat.getText();
        String dienTichXD = tbxDienTichXD.getText();
        String huong = tbxHuong.getText();
        String viTri = tbxViTriDiaOc.getText();
        String moTa = tbxMoTa.getText();
        String giaBan = tbxGiaBan.getText();
        if (diaChi.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenteraddressproperty"));
        }else if(dienTichDat.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenters1property"));
        }else if(dienTichXD.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenters2property"));
        }else if(huong.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenterdirectionproperty"));
        }else if(viTri.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenterlocationproperty"));
        }else if(moTa.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notenterdescribeproperty"));
        }else if(giaBan.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("notentercostproperty"));
        }else{
            try{
                Float d1 = Float.parseFloat(dienTichDat.trim());
                Float d2 = Float.parseFloat(dienTichXD.trim());
                Float g = Float.parseFloat(giaBan.trim());
                if(d1 > 0 && d2 > 0 && g > 0)
                    return true;
                else JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("conditionnumber"));
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("validnumber"));
            }
        }
        return false;
    }
    
