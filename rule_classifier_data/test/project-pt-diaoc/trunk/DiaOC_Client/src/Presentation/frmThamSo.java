    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(this.tblThamSo.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("mustselectGiaTien"));
        }
        else{
            String tenThamSo = tbxTenThamSo.getText().trim();
            String giaTri = tbxGiaTri.getText().trim();
            String giaiThich = tbxGiaiThich.getText().trim();
            if (tenThamSo.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("chuanhapquydinh"));
            }else if(giaTri.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("chuanhapgiatri"));
            }else if(giaiThich.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("chuanhapgiaithich"));
            }else{
                Float giaTriTS = Float.MIN_VALUE;
                try{
                    giaTriTS = Float.parseFloat(giaTri);
                    thamSo.Update(tenThamSo, giaTriTS, giaiThich);
                    this.LoadData();
                    tbxTenThamSo.setText("");
                    tbxGiaTri.setText("");
                    tbxGiaiThich.setText("");
                    JOptionPane.showMessageDialog(rootPane, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("dasuaquydinh"));
                }catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(rootPane, java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("giatriphailaso"));
                }catch(Exception e){
                    JOptionPane.showMessageDialog(rootPane, e.toString());
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

