    private void LoadData(){
        try
        {
            ArrayList<THAMSO> lst = new ArrayList<THAMSO>();
            lst = thamSo.GetAllRows();
            DefaultTableModel dtm = new DefaultTableModel(){
                @Override
                public boolean isCellEditable(int x,int y){
                    return false;
                }
            };
            dtm.addColumn("STT");
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("quydinh"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("giatri"));
            dtm.addColumn(java.util.ResourceBundle.getBundle("Presentation/" + frmMain.resource).getString("giaithich"));
            String s="";
            for(int i=0;i<lst.size();i++)
            {
                Vector _v = new Vector();
                THAMSO t = lst.get(i);
                _v.add(i+1);
                _v.add(t.getTenThamSo());
                _v.add(t.getGiaTri());
                _v.add(t.getGiaiThich());
                dtm.addRow(_v);
            }
            this.tblThamSo.setModel(dtm);
            this.tblThamSo.getColumnModel().getColumn(0).setPreferredWidth(10);
            this.tblThamSo.getColumnModel().getColumn(1).setPreferredWidth(30);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }

