package Entities;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class TOBUOM {
    private String MaToBuom = "";
    private String MaND = "";
    private String MaPhieuDangKy = "";
    private String MaGiaTien = "";

    /**
     */
    public TOBUOM(){}
    public TOBUOM(String maToBuom, String maND, String maPhieuDK, String maGiaTien){
        this.MaToBuom = maToBuom;
        this.MaND = maND;
        this.MaPhieuDangKy = maPhieuDK;
        this.MaGiaTien = maGiaTien;
    }
    
    @XmlElement
    public String getMaToBuom() {
        return MaToBuom;
    }

    public void setMaToBuom(String MaToBuom) {
        this.MaToBuom = MaToBuom;
    }

    @XmlElement
    public String getMaND() {
        return MaND;
    }

    public void setMaND(String MaND) {
        this.MaND = MaND;
    }

    @XmlElement
    public String getMaPhieuDangKy() {
        return MaPhieuDangKy;
    }

    public void setMaPhieuDangKy(String MaPhieuDangKy) {
        this.MaPhieuDangKy = MaPhieuDangKy;
    }

    @XmlElement
    public String getMaGiaTien() {
        return MaGiaTien;
    }

    public void setMaGiaTien(String MaGiaTien) {
        this.MaGiaTien = MaGiaTien;
    }

}
