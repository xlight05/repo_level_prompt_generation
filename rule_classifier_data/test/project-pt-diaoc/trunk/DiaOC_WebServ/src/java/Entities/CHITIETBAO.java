package Entities;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class CHITIETBAO {
    private String MaCT = "";
    private String MaQCBao = "";
    private String MaBao = "";
    
    public CHITIETBAO(){}
    public CHITIETBAO(String maCT, String maQCBao, String maBao){
        MaCT = maCT;
        MaQCBao = maQCBao;
        MaBao = maBao;
    }

    @XmlElement
    public String getMaCT() {
        return MaCT;
    }

    public void setMaCT(String MaCT) {
        this.MaCT = MaCT;
    }

    @XmlElement
    public String getMaQCBao() {
        return MaQCBao;
    }

    public void setMaQCBao(String MaQCBao) {
        this.MaQCBao = MaQCBao;
    }

    @XmlElement
    public String getMaBao() {
        return MaBao;
    }

    public void setMaBao(String MaBao) {
        this.MaBao = MaBao;
    }
}
