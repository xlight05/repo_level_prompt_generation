import java.util.Map;

public class QueryNodeTemplate {
	public QueryNodeTemplate(QueryNode queryNode, Map<String, QueryNode> vars) {
		this.queryNode = queryNode;
		this.varMappings = vars;
	}
	public QueryNode queryNode;
	public Map<String, QueryNode> varMappings = new HashMap<String,QueryNode>();
}
