package org.xfuze.tool.model;

/**
 * @author Jason Chan
 *
 */
public class Spring extends BaseObject {
	private String springName;

	private Model model;
	private Dao dao;
	private Dao auditDao;
	private DaoImpl daoImpl;
	private Service service;
	private ServiceImpl serviceImpl;

	public Model getModel() {
		return model;
	}

	public void setModel(Model model) {
		this.model = model;
	}

	public Dao getDao() {
		return dao;
	}

	public void setDao(Dao dao) {
		this.dao = dao;
	}

	public Dao getAuditDao() {
		return auditDao;
	}

	public void setAuditDao(Dao auditDao) {
		this.auditDao = auditDao;
	}

	public DaoImpl getDaoImpl() {
		return daoImpl;
	}

	public void setDaoImpl(DaoImpl daoImpl) {
		this.daoImpl = daoImpl;
	}

	public Service getService() {
		return service;
	}

	public void setService(Service service) {
		this.service = service;
	}

	public ServiceImpl getServiceImpl() {
		return serviceImpl;
	}

	public void setServiceImpl(ServiceImpl serviceImpl) {
		this.serviceImpl = serviceImpl;
	}

	public String getSpringName() {
		return springName;
	}

	public void setSpringName(String springName) {
		this.springName = springName;
	}

	public String getFileName() {
		return springName + ".xml";
	}

}
