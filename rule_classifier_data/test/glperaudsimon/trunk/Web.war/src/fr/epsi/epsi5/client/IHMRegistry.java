package fr.epsi.epsi5.client;

import fr.epsi.epsi5.client.pays.PaysWidget;
import fr.epsi.epsi5.client.ville.VilleWidget;

/**
 * IHMRegistry.
 * @author Wilhelm Peraud-Loïc Simon
 */
public class IHMRegistry {
	private static GoogleMapPanel mapPanel;
	private static WestWidget westWidget;
	private static CenterWidget centerPanel;
	private static PaysWidget paysPanel;
	private static VilleWidget villePanel;

	/**
	 * Getter pour obtenir le paysPanel.
	 * @return Panel des Pays
	 */
	public static PaysWidget getPaysPanel() {
		return paysPanel;
	}

	/**
	 * Setter du Panel des Pays.
	 * @param paysPanel Panel des Pays
	 */
	public static void setPaysPanel(PaysWidget paysPanel) {
		IHMRegistry.paysPanel = paysPanel;
	}

	/**
	 * Getter du Panel des Villes.
	 * @return Panel des Villes
	 */
	public static VilleWidget getVillePanel() {
		return villePanel;
	}

	/**
	 * Setter du Panel des Villes.
	 * @param villePanel Panel des Villes
	 */
	public static void setVillePanel(VilleWidget villePanel) {
		IHMRegistry.villePanel = villePanel;
	}

	/**
	 * Getter pour optenir le panelGoogle.
	 * @return mapPanel onglet google map
	 */
	public static GoogleMapPanel getMapPanel() {
		return mapPanel;
	}

	/**
	 * Setter pour affecter le panelGoogle.
	 * @param mapPanel onglet google map
	 */
	public static void setMapPanel(GoogleMapPanel mapPanel) {
		IHMRegistry.mapPanel = mapPanel;
	}
	
	/**
	 * Getter pour le centerPanel.
	 * @return Center Panel
	 */
	public static CenterWidget getCenterPanel() {
		return centerPanel;
	}
	
	/**
	 * Setter pour le centerPanel.
	 * @param centerPanel Center Panel à enregistrer
	 */
	public static void setCenterPanel(CenterWidget centerPanel) {
		IHMRegistry.centerPanel = centerPanel;
	}

	/**
	 * Getter pour le WestWidget.
	 * @return WestWidget de l'interface
	 */
	public static WestWidget getWestWidget() {
		return westWidget;
	}

	/**
	 * Setter pour le WestWidget.
	 * @param westWidget WestWidget de l'interface
	 */
	public static void setWestWidget(WestWidget westWidget) {
		IHMRegistry.westWidget = westWidget;
	}
}
