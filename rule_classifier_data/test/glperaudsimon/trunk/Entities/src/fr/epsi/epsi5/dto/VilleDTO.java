package fr.epsi.epsi5.dto;

import java.io.Serializable;

/**
 * Entité Ville.
 * @author Wilhelm Peraud-Loïc Simon
 */
public class VilleDTO implements Serializable {
	/**
	 * Default Serial ID.
	 */
	private static final long serialVersionUID = 1L;

	//L'id est générée automatiquement
	private int id;
	
	private String nom;
	
	private int idPays; 
		
	/**
	 * Constructeur de la classe Ville.
	 */
	public VilleDTO() {}
	
	/**
	 * Getter de l'attribut Id.
	 * @return Id de la Ville
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * Setter de l'attribut Id.
	 * @param id Id de la Ville
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	/**
	 * Getter de l'attribut Nom.
	 * @return Nom dela Ville
	 */
	public String getNom() {
		return nom;
	}

	/**
	 * Setter de l'attribut Nom.
	 * @param nom Nom de la Ville
	 */
	public void setNom(String nom) {
		this.nom = nom;
	}

	/**
	 * Setter de l'attribut IdPays.
	 * @param idPays Id du Pays
	 */
	public void setIdPays(int idPays) {
		this.idPays = idPays;
	}

	/**
	 * Getter de l'attribut IdPays.
	 * @return Id du Pays
	 */
	public int getIdPays() {
		return idPays;
	}
}
