package com.shautvast.realtimegc.util;



/**
 * A filename generator produces a file name. Can be a constant or any
 * dynamically created file name in case a log file name has for instance a
 * timestamp in it's filename.
 * 
 * @author shautvast
 */
public interface FileNameGenerator {
	/**
	 * @return
	 */
	public String getFileName();

}

