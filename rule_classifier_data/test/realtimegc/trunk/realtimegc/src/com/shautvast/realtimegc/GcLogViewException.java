package com.shautvast.realtimegc;

public class GcLogViewException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3910924793814666396L;

	public GcLogViewException() {
		super();
	}

	public GcLogViewException(String message, Throwable cause) {
		super(message, cause);
	}

	public GcLogViewException(String message) {
		super(message);
	}

	public GcLogViewException(Throwable cause) {
		super(cause);
	}
	
}
