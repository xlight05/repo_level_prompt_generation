package com.itzg.quidsee;

import java.awt.Font;

import javax.swing.SwingConstants;
import javax.swing.table.DefaultTableCellRenderer;

public class AligningTableCellRenderer extends DefaultTableCellRenderer {
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 * @param hAlignment a value from {@link SwingConstants}
	 */
	public AligningTableCellRenderer(int hAlignment) {
		setHorizontalAlignment(hAlignment);
		getFont().deriveFont(Font.PLAIN);
	}
}
