package org.jjsc;
/**
 * This exception arrives when users attempts to access the VM that either 
 * isn't started yet or already shutdown.
 * 
 * @author alex.bereznevatiy@gmail.com
 *
 */
public class NotStartedException extends ConfigurationException {
	private static final long serialVersionUID = 1L;
	
	public NotStartedException(String message) {
		super(message);
	}
}
