package pl.edu.agh.iosr.ftpserverremote.dataaccess;

/**
 * This is an exception that indicates thet the requested
 * entity was not found.
 * 
 * @author Tomasz Sadura
 */
public class EntityNotFoundException extends Exception {

  private static final long serialVersionUID = -1746659746678042633L;

  /**
   * Created a new <code>EntityNotFoundException</code>
   * @param message message of the exception
   */
  public EntityNotFoundException(final String message) {
    super( message );
  }
}
