
package pl.edu.agh.iosr.ftpserverremote.data;

/**
 *
 * @author Tomasz Jadczyk
 */
public class SpyUserData extends ServerData{

    private Long connectionDataID;
    private String date;
    private String message;

    public SpyUserData(Long connectionDataID) {
        this.connectionDataID = connectionDataID;
    }
    
    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
    
    public Long getConnectionDataID() {
	return connectionDataID;
    }
}
