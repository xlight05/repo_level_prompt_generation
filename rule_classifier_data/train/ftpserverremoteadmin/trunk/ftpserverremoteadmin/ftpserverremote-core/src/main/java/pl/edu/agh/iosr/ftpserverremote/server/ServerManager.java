package pl.edu.agh.iosr.ftpserverremote.server;

import org.apache.ftpserver.ftplet.FtpException;

/**
 * This is an interface for Server Manager, that handles information about the current server.
 * 
 * @author Tomasz Sadura
 *
 */
public interface ServerManager {

  /**
   * Saves information about current server. 
   */
  public void save() throws FtpException;

  /**
   * Returns current server id.
   */
  public Long getServerId();

}
