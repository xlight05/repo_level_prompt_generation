package pl.edu.agh.iosr;

/**
 * Thrown when some kind of operation is requested on server not present in current Context
 * 
 * @author Agnieszka Janowska
 */
public class ServerNotSupportedException extends Exception {
    
    public ServerNotSupportedException() {
	super();
    }
    
    public ServerNotSupportedException(String msg) {
	super(msg);
    }
}
